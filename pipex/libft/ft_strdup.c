#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*dup;
	int		i;
	int		length;

	length = 0;
	i = 0;
	while (s[length])
		length++;
	dup = (char *)malloc(sizeof(char) * (length + 1));
	if (!s || !dup)
		return (NULL);
	while (s[i])
	{
		dup[i] = s[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
